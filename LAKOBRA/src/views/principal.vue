<script setup>
import { ref, onMounted } from 'vue';

const anime = window.anime;

const tituloRef = ref(null);
const descripcionRef = ref(null);
const guitarRef = ref(null);
const logoRef = ref(null);

onMounted(() => {
  try {
    if (!anime) {
      console.error("Anime.js no se ha cargado.");
      return;
    }

    const guitarPaths = guitarRef.value.querySelectorAll('path');

    const timeline = anime.timeline({
      easing: 'easeOutExpo',
    });

    // 1. El título cae
    timeline
      .add({
        targets: tituloRef.value,
        translateY: [-30, 0],
        opacity: [0, 1],
        duration: 1200
      })
      // 2. Aparece la imagen del logo suavemente haciendo zoom
      .add({
        targets: logoRef.value,
        opacity: [0, 1],
        scale: [0.95, 1],
        easing: 'easeOutCubic',
        duration: 1000
      }, '-=600') 
      // 3. La guitarra se dibuja EXACTAMENTE al acabar el título
      .add({
        targets: guitarPaths,
        strokeDashoffset: [anime.setDashoffset, 0],
        easing: 'easeInOutSine',
        duration: 20500,
        delay: function (el, i) { return i * 10 },
      }, 1200)

      // 4. El texto aparece EXACTAMENTE al acabar el título (a la vez que la guitarra)
      .add({
        targets: descripcionRef.value.querySelectorAll('p'),
        translateY: [20, 0],
        opacity: [0, 1],
        delay: anime.stagger(200),
        duration: 2000
      }, 1200);

    // Animación continua del título (respiración)
    anime({
      targets: tituloRef.value,
      scale: [1, 1.02],
      textShadow: [
        '0 0 25px rgba(56, 189, 248, 0.15)',
        '0 0 45px rgba(56, 189, 248, 0.6)'
      ],
      direction: 'alternate',
      loop: true,
      easing: 'easeInOutSine',
      duration: 2500
    });

  } catch (error) {
    console.error('Error en la animación de Lakobra:', error);
  }
});
</script>

